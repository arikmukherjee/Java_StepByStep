public class Main {
    public static void main(String[] args) {
        String s1 = "5";
        String s2 = "10";
        System.out.println(s1 + s2); // Concatenation of strings, will print "510"

        int num1 = Integer.parseInt(s1); // Convert s1 to an integer
        int num2 = Integer.parseInt(s2); // Convert s2 to an integer
        System.out.println(num1 + num2); // Addition of integers, will print 15
    }
}
